a = float(input("Введите первое число: "))
b = input("Введите второе число: ")

adi = input("выберите какой знак будете использовать +, -, /, *, :")



if adi == "+":
        c = float(a) + float(b)
        print( c )
        print("спасибо")

elif adi == "-":
        c = float(a) - float(b)
        print( c )
        print("спасибо ")

elif adi == "*":
        c = float(a) * float(b)
        print( c )
        print("спасибо ")
elif adi == "/":
        c = float(a) / float(b)
        print( c)
        print("спасибо ")


